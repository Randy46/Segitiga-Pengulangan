// membuat pengulangan menggunakan nama angkot 1 - 10 
// menggunakan pengulangan "while"

var angkot = 1; // nomor angkot dimulai dari 1 (jika di kosongkan akan memulai dari 0)
var jumlahAngkot = 10; //jumlah angkot

while (angkot <= jumlahAngkot) {
    console.log('Angkot No.' + angkot + ' beroprasi dengan baik')
    angkot++;
}